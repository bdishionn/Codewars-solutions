// DESCRIPTION:
// You will be given an array a and a value x. All you need to do is check whether the provided array contains the value.

// Array can contain numbers or strings. X can be either.

// Return true if the array contains the value, false if not.

// My Solution:

public class Solution {

    public static boolean check(Object[] a, Object x) {
      
      Object searchVal = x; // value we are looking for in array represented as variable of Object x
      
        for(Object i : a){ // for each Object i in Array a
          if(i == searchVal){ // if Object i is equal to searchVal(value is present in array)
            return true; // we return true
          }
          
        }
        return false; // otherwise this will just return false
    }
}